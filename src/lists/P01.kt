package lists

/**********************************************************************************************
 * Find the last element of a list.
 ***********************************************************************************************/

fun <T> last(list: List<T>) = list.last()

fun main() {
    println(last(listOf(1, 1, 2, 3, 5, 8)))  // 8
    println(last(listOf("1", "1", "2", "3", "5", "8")))  // 8
}